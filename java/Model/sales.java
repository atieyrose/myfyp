public class sales {
    
    private int saleID;
    private int custID;
    private int prodID;
    private int quantity;
    private double total;
    
    public sales (int saleID, int custID, int prodID, int quantity, double total) {
        this.saleID = saleID;
        this.custID = custID;
        this.prodID = prodID;
        this.quantity = quantity;
        this.total = total;
    }

    public int getSaleID() {
        return saleID;
    }

    public void setSaleID(int saleID) {
        this.saleID = saleID;
    }

    public int getCustID() {
        return custID;
    }

    public void setCustID(int custID) {
        this.custID = custID;
    }

    public int getProdID() {
        return prodID;
    }

    public void setProdID(int prodID) {
        this.prodID = prodID;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
    
    
    
}
